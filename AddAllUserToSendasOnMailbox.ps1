#takes a mailbox and assigns sendas perms to all mailboxes labeled as usermailboxes

#list of user accounts that are not actually users
$notUser='example','example2'

#grab all mailboxes that are user mailboxes
$allmailboxes = Get-Mailbox -RecipientTypeDetails UserMailbox |  Select-Object Alias
$i = 0;
$totalUsers = $allmailboxes.Count

#choose the mailbox that these perms will be applied to
$mailbox = Read-Host "Enter the Email address of the mailbox you would like to change permissions on"

#iterate through them
foreach($user in $allmailboxes)
{
    $i++;
    Write-Progress -activity "Processing $($user.Alias)" -status "$i out of $totalUsers completed"
    If($($user.Alias) -in $notUser) #check if user mailbox is not really a user mailbox according to the list
    {
    Write-Host( $($user.Alias) + " User Skipped") -f Yellow
    continue
    }
    Else
    {
    Write-Host($($user.Alias) + " added to SendAs permissions on " + $mailbox) -f Green
    Add-RecipientPermission -Id $mailbox -AccessRights SendAs -Trustee $($user.Alias) -confirm:$false #add permission for $user to $mailbox
    }
}
