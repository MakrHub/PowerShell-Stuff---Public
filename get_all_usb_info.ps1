#Pulls Vendor ID and Product ID in Hex and Decimal
#Pulls Device Serial Number
#only pulls this information for currently connected usb devices that windows recognizes as mass storage devices

$deviceInfo = gwmi Win32_USBControllerDevice |%{[wmi]($_.Dependent)} | where-object {$_.Status -eq 'OK'} | Select DeviceID | Out-String -Stream | Select-String -Pattern 'VID'
Foreach($device in $deviceInfo)
{
$device = $device.ToString()
$device = $device.Split("\")
$SerialNum = $device[2]
$VIDandPID = $device[1].Split("&")
$Vid = $VIDandPID[0].Split("_")
$ProductID = $VIDandPID[1].Split("_")
$Vid = $Vid[1]
$ProductID = $ProductID[1]
$Vid_Dec = [int]"0x$Vid"
$ProductID_Dec = [int]"0x$ProductID"
Write-Host("Vendor ID in Hex: " + $Vid) -ForegroundColor Yellow
Write-Host("Vendor ID in Dec: " + $Vid_Dec) -ForegroundColor Yellow
Write-Host("Product ID in Hex: " + $ProductID) -ForegroundColor Cyan
Write-Host("Product ID in Dec: " + $ProductID_Dec) -ForegroundColor Cyan
Write-Host("Serial Number: " + $SerialNum + "`r") -ForegroundColor Green
}
